function wybor() {
	let cyfra = Number(document.getElementById("cyfra").value);
	switch (cyfra) {
		case 0:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor navy";
			document.body.style.backgroundColor = "navy";
			break;

		case 1:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor czerwony";
			document.body.style.backgroundColor = "red";
			break;

		case 2:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor zielony";
			document.body.style.backgroundColor = "green";
			break;

		case 3:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor niebieski";
			document.body.style.backgroundColor = "blue";
			break;

		case 4:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor zolty";
			document.body.style.backgroundColor = "yellow";
			break;

		case 5:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor fioletowy";
			document.body.style.backgroundColor = "violet";
			break;

		case 6:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor rozowy";
			document.body.style.backgroundColor = "pink";
			break;

		case 7:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor szary";
			document.body.style.backgroundColor = "gray";
			break;

		case 8:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor brazowy";
			document.body.style.backgroundColor = "brown";
			break;

		case 9:
			document.getElementById("id-wynik").innerHTML =
				"Dla cyfry zero dopasowano kolor pomaranczowy";
			document.body.style.backgroundColor = "orange";
			break;
	}
}

